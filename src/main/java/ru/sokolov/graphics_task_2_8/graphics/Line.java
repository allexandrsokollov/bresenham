package ru.sokolov.graphics_task_2_8.graphics;

public class Line {
    private final int x0;
    private final int y0;
    private final int x1;
    private final int y1;


    public Line(int x0, int y0, int x1, int y1) {
        this.x0 = x0;
        this.y0 = y0;
        this.x1 = x1;
        this.y1 = y1;
    }
}
